    private void tableOdontMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableOdontMouseClicked
        /* busca a linha selecionada no jTable1 */
        int linhasel = this.tableOdont.getSelectedRow();
        int colunachave = 3; // configura coluna como 0,isto é, a primeira coluna
        Object chave = this.tableOdont.getModel().getValueAt(linhasel, colunachave);
        /* Informações Relevantes sobre capturar dados do Jcombobox:
        Pegar pelo índice : this.cmbnivel.setSelectedIndex(resul.getInt("idnivel"));
        Pegar pelo item : this.cmbnivel.setSelectedItem("Operador");
        Número da linha : this.cmbnivel.setSelectedIndex(jTable1.getSelectedRow());*/
        Dentistas Den = new Dentistas();
        Den.setCro((String) chave);
        DentistaDao dd = new DentistaDao();
        ResultSet resul = dd.buscar(Den);
        try {
            if (resul.next()) {
                
                this.textNome.setText(resul.getString("nome"));
                this.textEmail.setText(resul.getString("email"));
                this.textEspec.setText(resul.getString("especialidade"));
                this.textCRO.setText(resul.getString("cro"));
                this.txtSenha.setText(resul.getString("senha"));
                
            } else {
                JOptionPane.showMessageDialog(null, "Registro não encontrado!");
                this.textCRO.grabFocus();
            }
        } catch (SQLException err) {
            JOptionPane.showMessageDialog(null, "Erro ao navegar na Jtable! ");
        }
    }//GEN-LAST:event_tableOdontMouseClicked

