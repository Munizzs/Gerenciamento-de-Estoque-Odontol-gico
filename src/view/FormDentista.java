    @SuppressWarnings("unchecked")

    private void carregar_usuarios() {
        DentistaDao dd = new DentistaDao();

        while (tableOdont.getModel().getRowCount() > 0) {
            ((DefaultTableModel) tableOdont.getModel()).removeRow(0);
        }
        try {
            ResultSet todos = dd.buscartodos();
            DefaultTableModel tab = (DefaultTableModel) this.tableOdont.getModel();
            while (todos.next()) {
                Object[] linha = {todos.getString("nome"), todos.getString("email"),
                    todos.getString("especialidade"),
                    todos.getString("cro")};
                tab.addRow(linha);
            }
            todos.close();
        } catch (SQLException err) {
            JOptionPane.showMessageDialog(null, err.getMessage());
        }
    }
