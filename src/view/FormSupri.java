    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
        SuprimentosDao sd = new SuprimentosDao();
        Suprimentos sm = new Suprimentos();
       
        sm.setNomeEquip(this.txtNome.getText());
        if(checkReu.isSelected()){
            sm.setReutilizavel(true);
        }else{
            sm.setReutilizavel(false);
        }
        
        
        ResultSet resul = sd.buscar(sm);
        try {
            if (resul.next()) {
                sd.alterar(sm);
                
            } else {
                sd.incluir(sm);
                 
            }
        } catch (SQLException err) {
            JOptionPane.showMessageDialog(null,
                    err.getMessage());
        }
        /* Preenche o Jtable no form */
        this.carregar_suprimentos();
        
    }//GEN-LAST:event_btnAddMouseClicked

